package Lesson1.Operators;

public class ArithmeticOperators2 {
    public static void main(String[] args) {
        //declare(elan edilen) variables
        int a=12,b=5;

        //addition oparator
        System.out.println("a+b="+(a+b));

        //subtraction oparator
        System.out.println("a-b="+(a-b));

        //multiplication operator
        System.out.println("a*b="+(a*b));

        //division operator
        System.out.println("a/b="+(a/b));

        //module operation
        System.out.println("a%b="+(a%b));

    }
}
